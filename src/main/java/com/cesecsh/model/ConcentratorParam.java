package com.cesecsh.model;

public class ConcentratorParam {

	private String conParamId;
	private String conParam;	
	private String conValue;
	private String conId;
	public String getConParamId() {
		return conParamId;
	}
	public void setConParamId(String conParamId) {
		this.conParamId = conParamId;
	}
	public String getConParam() {
		return conParam;
	}
	public void setConParam(String conParam) {
		this.conParam = conParam;
	}
	public String getConValue() {
		return conValue;
	}
	public void setConValue(String conValue) {
		this.conValue = conValue;
	}
	public String getConId() {
		return conId;
	}
	public void setConId(String conId) {
		this.conId = conId;
	}
	
	
}